package ElseIf;

public class Prog1 {
	public static void main(String[] args) {
		
		  
	     
	     Float a = 56f, b = 21f;
	     char operation= '+';
	     switch(operation)
	      { 
	        case '+':
	        float d = a+b;
	         {
	           System.out.println("Sum of above integer is " + d);
	         } 
	         break;
	       case '*': 
	        float g = a*b;
	         {    
	           System.out.println("multiplication is " + g);
	         }
	         break;
	        case '-':
	         float t = a-b;
	         {  
	          System.out.println("Substraction is "+ t);
	         }
      	}
	}
	
}
