package UseOfWhileLoop;

public class WhileLoop1 {

	public static void main(String[] args) {
		int i =17, j=1;
		while(j<=10)
		{
			System.out.println(i + "*" + j + "=" + i*j);
			j++;
		}
		

	}

}
