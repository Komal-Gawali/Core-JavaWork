package UseOfTools;

public class LocalLoop1 {

	public static void main(String[] args) {
	   for(int k=1; k<=5; k++ )
		   // 1)variable declaration, 2) condition, 3) Increment/decrement
	     {
		   System.out.print( k + "  " );
	     }
	   System.out.println("_____");
	   for(int i=1; i <= 50; i= i + 2)
		   System.out.print(i + " ");
       }
    }

