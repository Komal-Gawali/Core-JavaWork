package LogicalProgram;

public class SwapsTwoNum {

	public static void main(String[] args) {
		
		int a = 14, b = 55;
		System.out.println("Orignal value A = " + a + " " + " & B = " + b);
		int tamp = a;
		a = b;
		b = tamp;
		System.out.println("After SwappUps A = " + a + " " + " & B " + b);
	}

}
